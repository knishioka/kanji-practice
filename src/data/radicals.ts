// 部首を名前で検索
export function findRadicalByName(name: string): Radical | undefined {
  return radicals.find((r) => r.name === name || r.aliases?.includes(name) || r.char === name);
}

// 部首を文字で検索
export function findRadicalByChar(char: string): Radical | undefined {
  return radicals.find((r) => r.char === char || r.variants?.includes(char));
}
